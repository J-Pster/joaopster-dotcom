export type UiKey = keyof (typeof ui)['en'];

export function getLangFromUrl(url: URL): Lang {
  const [, lang] = url.pathname.split('/');
  if (lang in languages) return lang as Lang;
  return defaultLang;
}

export function useTranslations(lang: Lang) {
  return function t(key: UiKey): string {
    return (ui[lang][key] ?? ui[defaultLang][key]) as string;
  };
}

export function getLocalePath(currentPath: string, targetLang: Lang): string {
  const nonDefaultLocales = Object.keys(languages).filter(l => l !== defaultLang);
  let stripped = currentPath;
  for (const locale of nonDefaultLocales) {
    if (currentPath.startsWith(`/${locale}/`)) {
      stripped = currentPath.slice(locale.length + 1);
      break;
    } else if (currentPath === `/${locale}`) {
      stripped = '/';
      break;
    }
  }
  if (targetLang === defaultLang) return stripped;
  return `/${targetLang}${stripped}`;
}
